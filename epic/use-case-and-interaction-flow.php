		<h3>Use Case</h3>
		<p><strong>Title: </strong>Adding Album to Wishlist.</p>
		<p><strong>Description: </strong>Eddie's collection of music has gotten stale. He is looking for some new music to add to his wishlist so he can purchase the digital or physical albums at a later date.</p>
		<p><strong>Precondition: </strong>User must have a Bandcamp account and be logged in.</p>
		<p><strong>Postcondition: </strong>Album added to Wishlist Collection</p>
	</div>
	<div>
		<h3>Interaction Flow</h3>
	<ol>
		<li>User connects to Bandcamp.</li>
		<li>Site displays Eddies profile.</li>
		<li>User clicks search bar and selects a specific genre.</li>
		<li>Site displays multiple genres in a drop-down list.</li>
		<li>User selects Genre from drop-down list.</li>
		<li>Site displays multiple artists from genre catagory.</li>
		<li>User browses results until he has found a band/musician that he likes, he clicks on the page.</li>
		<li>Site displays band/musicians album page.</li>
		<li>User clicks the wishlist button.</li>
		<li>Site adds album to wishlist collection tied to profile.</li>
	</ol>
	</div>
